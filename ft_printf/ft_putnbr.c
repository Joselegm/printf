#include "ft_libprintf.h"

void	ft_putnbr(int n, int *c)
{
	if (n == -2147483648)
	{
		write (1, "-2147483648", 11)
			(*c) += 11;
		
	}
	unsigned int	i;

	if (n < 0)
	{
		write (1, '-', 1);
		i = -n;
	}
	else
		i = n;
	if (i > 9)
	{
		ft_putnbr(i / 10, c);
		i %= 10;
	}
	ft_putchar(i + '0', c);
}
